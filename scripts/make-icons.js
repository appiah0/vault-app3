#!/usr/bin/env node
// scripts/make-icons.js
// Run: node scripts/make-icons.js
// Generates placeholder PNG icons using canvas (optional step)
// If you skip this, Expo will use a default icon

const fs = require('fs');
const path = require('path');

// Create assets dir if missing
const assetsDir = path.join(__dirname, '..', 'assets');
if (!fs.existsSync(assetsDir)) fs.mkdirSync(assetsDir, { recursive: true });

// Create minimal 1x1 PNG (valid PNG header)
// Replace with real 1024x1024 PNG for production
const minPng = Buffer.from([
  0x89,0x50,0x4E,0x47,0x0D,0x0A,0x1A,0x0A, // PNG signature
  0x00,0x00,0x00,0x0D,0x49,0x48,0x44,0x52, // IHDR chunk
  0x00,0x00,0x00,0x01,0x00,0x00,0x00,0x01, // 1x1
  0x08,0x02,0x00,0x00,0x00,0x90,0x77,0x53, // 8bit RGB
  0xDE,0x00,0x00,0x00,0x0C,0x49,0x44,0x41, // IDAT
  0x54,0x08,0xD7,0x63,0x60,0x60,0x60,0x00,
  0x00,0x00,0x04,0x00,0x01,0x27,0x07,0x17,
  0x9C,0x00,0x00,0x00,0x00,0x49,0x45,0x4E, // IEND
  0x44,0xAE,0x42,0x60,0x82
]);

['icon.png','adaptive-icon.png','splash.png','favicon.png'].forEach(name => {
  const p = path.join(assetsDir, name);
  if (!fs.existsSync(p)) {
    fs.writeFileSync(p, minPng);
    console.log('Created placeholder:', name);
  }
});

console.log('Done. Replace assets/*.png with real 1024x1024 icons for production.');
